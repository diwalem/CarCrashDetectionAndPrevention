    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) policeFrontJTable.getModel();

        model.setRowCount(0);

        for (WorkRequest request : policeFrontDeskOrganization.getWorkQueue().getWorkRequestList()) {
            if (request instanceof HospitalToCustomerServiceWorkRequest) {
                System.out.print("");
            } else if (request instanceof PoliceToCustomerServiceWorkRequest) {
                System.out.print("");
            } else {

                Object[] row = new Object[6];

                row[1] = request.getPatientName();
                row[0] = request;
                row[2] = request.getPriority();
                row[3] = request.getSender();
                row[4] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
                row[5] = request.getStatus();

                model.addRow(row);

            }
        }
    }

