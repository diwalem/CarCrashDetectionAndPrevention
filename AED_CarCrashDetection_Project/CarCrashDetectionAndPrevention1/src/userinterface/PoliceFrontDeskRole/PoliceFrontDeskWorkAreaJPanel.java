        int row = policeFrontJTable.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
         WorkRequest request = (WorkRequest) policeFrontJTable.getValueAt(row, 0);
         if (request.getStatus().equals("Received")) {
            JOptionPane.showMessageDialog(null, "Please assign the request to yourself before sending to the police.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
          JOptionPane.showMessageDialog(null, "Sent to Save Lives.", "Information", JOptionPane.INFORMATION_MESSAGE);
           
            int size1 = policeFrontDeskOrganization.getWorkQueue().getWorkRequestList().size();

            if (policeFrontDeskOrganization.getWorkQueue().getWorkRequestList() != null && !policeFrontDeskOrganization.getWorkQueue().getWorkRequestList().isEmpty()) {

                WorkRequest request1 = policeFrontDeskOrganization.getWorkQueue().getWorkRequestList().get(size1 - 1);

                if (request1 instanceof PoliceToCustomerServiceWorkRequest) {
                    PoliceToCustomerServiceWorkRequest wr2 = new PoliceToCustomerServiceWorkRequest();

                    wr2.setPatientName(request1.getPatientName());
                    wr2.setLocation(request1.getLocation());
                    wr2.setDescription(((PoliceToCustomerServiceWorkRequest) request1).getDescription());
                    wr2.setOthers(((PoliceToCustomerServiceWorkRequest) request1).getOthers());
                    wr2.setReason(((PoliceToCustomerServiceWorkRequest) request1).getReason());
                    wr2.setDamage(((PoliceToCustomerServiceWorkRequest) request1).getDamage());

                    Organization org = null;
                    for (Network network : ecoSystem.getNetworkList()) {

                        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                            for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                                if (organization instanceof CustomerServiceOrganization) {
                                    org = organization;
                                    break;
                                }
                            }
                            

                        }
                    }
                    if (org != null) {
                                org.getWorkQueue().getWorkRequestList().add(wr2);

                            }

                }
            }
        }


